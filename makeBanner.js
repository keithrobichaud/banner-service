function banner(str, emoji1, emoji2 = ':white_square:') {
	var args = [emoji2, emoji1];
	var output = "";
	for (var i = 0; i < str.length; i++) {
		var functionName = '';
		var char = str.charAt(i);

		if (char === ' ') {
			functionName = 'blank';
		} else {
			functionName = 'make' + char.toUpperCase();
		}
		output += window[functionName](...args);
	}
	output += line(...args);
	return output;
}

module.exports = banner;
